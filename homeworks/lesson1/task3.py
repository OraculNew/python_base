"""
Узнайте у пользователя число n. Найдите сумму чисел n + nn + nnn. Например, пользователь ввёл число 3. 
Считаем 3 + 33 + 333 = 369.
"""

# Объявление переменных
n: int = 0
summa: int = 0

# Код программы
n = input("Введите целое число \n>>>:")

if n and type(n) == str:
    summa = int(n)+int(n*2)+int(n*3)
    print(summa)
